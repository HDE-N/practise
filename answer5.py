import pandas as pd

# 讀取兩日的資料
df1 = pd.read_csv('./file/STOCK_DAY_ALL_20260401.csv')
df2 = pd.read_csv('./file/STOCK_DAY_ALL_20260402.csv')

# 清理資料：確保計算的欄位為數值格式
cols_to_numeric = ['收盤價', '開盤價', '成交筆數', '成交金額']
for col in cols_to_numeric:
    df1[col] = pd.to_numeric(df1[col], errors='coerce')
    df2[col] = pd.to_numeric(df2[col], errors='coerce')

# 透過證券代號與名稱合併兩日資料，後綴 _1 代表 4/1，_2 代表 4/2
df = pd.merge(df1, df2, on=['證券代號', '證券名稱'], suffixes=('_1', '_2'))

# =================================================================
# 1. 「元大台灣50（0050）」在 4/2 收盤價比 4/1 跌了多少，並印出差值
# =================================================================
s0050 = df[df['證券代號'].astype(str).str.contains('0050')]

print("第 1 題輸出：")
if not s0050.empty:
    c1 = s0050['收盤價_1'].values[0]
    c2 = s0050['收盤價_2'].values[0]
    diff = c2 - c1  # 算出差值
    print(f"{c1} {c2} {diff:.2f}")
print("-" * 20)

# =================================================================
# 2. 4/2 筆數 >= 4/1 筆數 * 2 的股票，依成長倍數降冪前三名
# =================================================================
df['成長倍數'] = df['成交筆數_2'] / df['成交筆數_1']
growth_stocks = df[df['成交筆數_2'] >= df['成交筆數_1'] * 2]

top3_growth = growth_stocks.sort_values('成長倍數', ascending=False).head(3)

print("第 2 題輸出：")
for _, row in top3_growth.iterrows():
    print(f"{row['證券代號']} {row['成長倍數']:.2f}")
print("-" * 20)

# =================================================================
# 3. 連續兩天「收盤價」都大於當日「開盤價」的總數量
# =================================================================
red_k_stocks = df[(df['收盤價_1'] > df['開盤價_1']) & (df['收盤價_2'] > df['開盤價_2'])]

print("第 3 題輸出：")
print(len(red_k_stocks))
print("-" * 20)

# =================================================================
# 4. 累計成交總金額最高的前三名股票
# =================================================================
df['總金額'] = df['成交金額_1'] + df['成交金額_2']
top3_amount = df.nlargest(3, '總金額')

print("第 4 題輸出：")
for _, row in top3_amount.iterrows():
    print(f"{row['證券名稱']} {int(row['總金額'])}")