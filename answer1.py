import pandas as pd

# 讀取資料
df = pd.read_csv('./file/c226a6c9ac2849d94e58dbeeefb6b91f_export.csv')

# 資料清理：將「癌症發生數」的千分位逗號去除，並轉換為數值型態
df['癌症發生數'] = pd.to_numeric(df['癌症發生數'].astype(str).str.replace(',', ''), errors='coerce')

# =================================================================
# 1. 找出這份資料中，總共包含了哪幾種「癌症別」？
# =================================================================
cancer_types_count = df['癌症別'].nunique()
print("第 1 題輸出：")
print(cancer_types_count)
print("-" * 20)

# =================================================================
# 2. 計算「2000 年至 2005 年」間，「台北市」罹患「胃」癌的總人數
# =================================================================
# 使用條件篩選，並將性別限定為 '全' 避免男女分開計算與總計重複相加
q2_mask = (
    (df['癌症診斷年'].between(2000, 2005)) & 
    (df['縣市別'] == '台北市') & 
    (df['癌症別'] == '胃') & 
    (df['性別'] == '全')
)
stomach_cancer_total = df[q2_mask]['癌症發生數'].sum()
print("第 2 題輸出：")
print(int(stomach_cancer_total))
print("-" * 20)

# =================================================================
# 3. 「2022年」，「全癌症」發生總數最高的前三名縣市以及其數量
# =================================================================
# 篩選特定年份與癌症別，排除全國，並將性別限定為 '全'
q3_mask = (
    (df['癌症診斷年'] == 2022) & 
    (df['癌症別'] == '全癌症') & 
    (df['縣市別'] != '全國') & 
    (df['性別'] == '全')
)
df_q3 = df[q3_mask]

# 依發生數量由大到小排序，取前三名
top3_cities = df_q3.nlargest(3, '癌症發生數')[['縣市別', '癌症發生數']]

print("第 3 題輸出：")
for index, row in top3_cities.iterrows():
    print(f"{row['縣市別']} {int(row['癌症發生數'])}")